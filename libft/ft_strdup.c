#include <stdlib.h>
#include "libft.h"

char	*ft_strdup(char *src)
{
	char	*dest;
	int		sizestr;
	int		i;

	sizestr = ft_strlen(src);
	dest = malloc((sizestr + 1) * sizeof(*dest));
	i = 0;
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
